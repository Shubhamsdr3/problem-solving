package greedyalgorithm;

public class MyUtils {

    public static int sumArray(Integer[] arr) {
        int sum =0 ;
        for (int val: arr) {
            sum = sum + val;
        }
        return sum;
    }

    public static int sumArray(int[] arr) {
        int sum =0 ;
        for (int val: arr) {
            sum = sum + val;
        }
        return sum;
    }
}
